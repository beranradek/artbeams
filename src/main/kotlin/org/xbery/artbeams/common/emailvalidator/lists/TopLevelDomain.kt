    private fun check() {
        val url = URI("http://data.iana.org/TLD/tlds-alpha-by-domain.txt").toURL()
        val con = url.openConnection()
        val `in` = con.getInputStream()
        var encoding =
            con.contentEncoding // ** WRONG: should use "con.getContentType()" instead but it returns something like "text/html; charset=UTF-8" so this value must be parsed to extract the actual encoding
        encoding = encoding ?: "UTF-8"
        val baos = ByteArrayOutputStream()
        val buf = ByteArray(8192)
        var len: Int
        while ((`in`.read(buf).also { len = it }) != -1) {
            baos.write(buf, 0, len)
        }
        val body = String(baos.toByteArray(), charset(encoding))

        var missingCnt = 0
        val scanner = Scanner(body)
        while (scanner.hasNext()) {
            val line = scanner.nextLine()
            if (!line.startsWith("#")) {
                //if(!MAP.containsKey(line.toLowerCase())) {
                if (!domains.contains(line.lowercase(Locale.getDefault()))) {
                    //System.out.println("{\"" + line.toLowerCase() + "\", new TopLevelDomain(\"" + line.toLowerCase() + "\", Type.UNKNOWN, null)},");
                    println("domains.add(\"" + line.lowercase(Locale.getDefault()) + "\");")
                    missingCnt++
                }
            }
        }
        scanner.close()
        println("Missing $missingCnt TLDs")
    }

